    /**
     * This a callback object for the {@link ImageReader}. "onImageAvailable" will be called when a
     * still image is ready to be processed.
     */
    private final ImageReader.OnImageAvailableListener mOnImageAvailableListener
            = new ImageReader.OnImageAvailableListener() {

        private boolean mIsImagePreviewLoaded;
        private long mLastScannedTime;

        @Override
        public void onImageAvailable(final ImageReader reader) {
            if (!mIsImagePreviewLoaded) {
                final Image img = reader.acquireLatestImage();
                mIsImagePreviewLoaded = true;

                final long currentTime = System.currentTimeMillis();

                // For optimization reasons we don't process every single frame
                if (currentTime - mLastScannedTime > FRAME_TIMEOUT && !mIsQrCodeProcessing) {
                    mLastScannedTime = currentTime;
                    mIsQrCodeProcessing = true;
                    mImageProcessingHandler.post(new Runnable() {
                        @Override
                        public void run() {
                            int result = 0;
                            try {
                                ByteBuffer buffer = img.getPlanes()[0].getBuffer();
                                byte[] data = new byte[buffer.remaining()];
                                buffer.get(data);

                                net.sourceforge.zbar.Image barcode = new net.sourceforge.zbar.Image(img.getWidth(), img.getHeight(), "Y800");
                                barcode.setData(data);
                                result = mScanner.scanImage(barcode);
                                //if we have meaningful result
                                if (result != 0) {
                                    // Clear all pending runnables
                                    mImageProcessingHandler.removeCallbacksAndMessages(null);
                                    SymbolSet symbolSet = mScanner.getResults();
                                    String url = null;
                                    for (Symbol sym : symbolSet) {
                                        url = sym.getData();
                                    }
                                    mListener.onQrResult(url);
                                }
                            } catch (NullPointerException ex) {
                                ex.printStackTrace();
                            } finally {
                                if (img != null)
                                    img.close();
                                mIsImagePreviewLoaded = false;
                            }

                            if (result == 0) {
                                mIsQrCodeProcessing = false;
                            }
                        }
                    });
                } else if (img != null) {
                    img.close();
                    mIsImagePreviewLoaded = false;
                }
            }
        }
    };

